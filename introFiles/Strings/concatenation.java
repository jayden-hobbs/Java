package Strings;

public class concatenation {
    public static void main(String[] args) {
        String firstName = "Jayden";
        String lastName = "Hobbs";
        System.out.println(firstName + " " + lastName); // prints Jayden Hobbs

        String uni1 = "Leeds";
        String uni2 = " Beckett";
        System.out.println(uni1.concat(uni2)); // prints Leeds Beckett
    }
}
