package singleFiles;
public class printTypes {
    public static void main(String[] args) {
    System.out.print("Hello");
    System.out.print(" World!"); // print() does not add a new line so this will print as Hello World on the same line


    System.out.print("\n\n"); // print a blank line


    System.out.println("Hello");
    System.out.println("World!"); // println() adds a new line so this will print as Hello on one line and World on the next

    System.out.print("\n\n"); // print a blank line

    System.out.println(2); // prints the number 2 - you don't need " " when printing numbers
    System.out.println(2*5); // prints 2*5 = 10 - you don't need " " when printing calculations
}
    
}
